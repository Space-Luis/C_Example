#include <stdio.h>

int main(void){

    int A = 1; //정수 A값에 1 대입
    int B = 2; //정수 B값에 2 대입

    printf("=============== < 연산자 테스트 > ===============\n");
    printf("A + B = %d", A + B); //A + B의 값 출력
    printf("A - B = %d", A - B); //A - B의 값 출력
    printf("A * B = %d", A * B); //A * B의 값 출력
    printf("A / B = %d", A / B); //A / B의 값 출력
    
    /* 
    + : 더하기 
    - : 빼기
    * : 곱하기
    / : 나누기
    % : 나머지
    */

    return 0; //0이라는 값을 반환 합니다
}